                    <div class="menu-images "><img src="https://36.media.tumblr.com/47514f75bd27eee85905be08781c1c37/tumblr_ntee57Pef51ud7rr3o1_1280.jpg" alt="CheeseBaconWar"></div>
                    <div class="menu-titles"><h1 class="">HomeMade Organic Pesticides for Tomatoes</h1></div>
                    <div class="menu-items ">                  
						<ul>
							<li>Use homemade organic pesticides to control garden trouble makers on your gardens.</li>
							<li>Tomato plants can suffer from insect infestations, including flea beetles, tomato hornworms and other leaf-chewing insects, aphids, whitefly and mites, as well as fungal diseases, such as early blight and powdery mildew.</li>
							<li>When growing tomatoes, you also have to battle common garden problems with weeds and pesky slugs. Make your own inexpensive organic pesticides to manage and control these tomato growing challenges.</li>
						</ul>
						<h4>Insect and Mite Control</h4>
							<p>To manage problems with aphids and mites, try a pesticide made with rhubarb leaves. Simply boil rhubarb leaves in water for 20 minutes, and when cool strain into a spray bottle. A mild dish washing soap can be added as well. For managing leaf-chewing pests, mix up mashed chili peppers, chopped onion, garlic, ash, nimtree leaves, soap in ten (10) litres of water for seven (7) days before straining and spraying tomato plants. To curb attacks from tomato hornworms and other leaf cutters, make a mash of marigold leaves and flowers, and soak in water for 24 hours. Strain the solids, and add another 1.5 quarts of water plus a pinch of liquid castille soap before spraying. For problems with beetles, caterpillers, whitefly and any self-bodied insect pest, use a mix of water, cayene peppers and chopped horseradish toot or rare chicken!.</p>
						<h4>Fungal Preventation</h4>
						<ul>
							<li>Fungal diseases, like powdery mildew, can be prevented using a spray made of baking soda or potassium bicarbonate, horticultural oil and water. If you don't have horticultural oil, citrus oil or molasses makes a good substitute. In addition, milk daters powdery mildew. Mix 1(one) part of milk(1cup) to 2 cups of water in a spray bottle for easy application.</li>
							<li>Cornmeal also can be used to manage fungal infections. Mix 1 cup of cornmeal with 5 gallons of water, strain, and then spray on tomato plants. For warding off early blight, mix 2 table spoons each of cooking oil, organic baby shampoo and baking soda with 1 gallon of water, and then spray both sides of the leaves for best prevention.</li>
						</ul>
							<p>A good rule of thumb is to apply a pesticide to just a small area of the plant first as a test.</p>
							<p>Pests can be managed with cow-urine spray brewed for three months.</p>
							<p>Benefits of organic farming include: less pesticide-related illness, improved household nutrition and gender equality.</p>
