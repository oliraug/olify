                    <div class="menu-titles"><h1 class="">Soil nutrients needed by tomatoes</h1></div>
                    <div class="menu-items ">
                    <ul>  
					<li>1.	Primary nutrients</li>
					<ul>
						<li>Nitrogen – Plant growth and chlorophyll production depend on nitrogen.</li>
						<li>Phosphorus – Helps tomatoes grow and cope with stress while aiding in energy production.</li>
						<li>Potassium – Fights disease, improves tomato quality and is also important to photosynthesis’; possible sources of potassium is ash.</li>
					</ul>
					<li>2.	Tomatoes need the “secondary nutrients”, calcium, magnesium and sulfur, but in lesser amounts than the primary nutrients. </li>
					</ul>
					<p>Calcium improves cell health, protecting against diseases and bruises. Tomatoes with higher levels of calcium are also more nutritious. Photosynthesis and chlorophyll both rely on magnesium, which helps their overall quality.</p>
					<p>Sulfur is needed for proteins and amino acids; a deficiency in magnesium and sulfur harms growth and causes yellow leaves.</p>
					<h5>Micronutrients</h5>
					<p>Tomatoes need micronutrients too, even if only in small amounts. Zinc, iron, boron, chloride, molybdenum, copper, and manganese serve many purposes. Molybdenum for instance helps tomatoes efficiently use nitrogen, while zinc helps regulate growth and promotes proper sugar consumption. Boron assists tomatoes in making use of nutrients and is also essential to the development of their fruit and seeds.</p>
					<p>Micronutrients can be delivered through organic matter. Work things like grass cuttings and old leaves into the tomato’s soil or spread them around the plants as mulch. As they decompose, they will release the needed nutrients.</p>
