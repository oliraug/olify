            <h2 class="section-title text-center" style="font-family: Beyond;">Our Story</h2>
            <p class="lead main text-center">It’s nice to be able to save your own tomato seeds.</p>
            <div class="row text-center story">
                <div class="col-sm-4">
                    <div class="col-wrapper">
                        <div class="icon-wrapper"> <i class="fa fa-anchor"></i> </div>
                        <h3>EST. 2013</h3>
                        <p>You may have bought home a particular delicious tomato from the supermarket, or gotten an heirloom tomato from a farmer’s market, or grown one in your own garden that is so wonderful you want to save the seeds from it and grow them the next season/year. Nothing ever tastes quite as good as a home-grown tomato!. So, how do you save the seeds? The method is easy to do… it’s a little gloppy ( a soft soggy mixture, as of food), and it’s a little funky, but you’ll be able to save seeds in a manner that will lesson the occurrence of tomato disease while giving you plenty of seeds to germinate, and with left-over to share or trade. This seed saving process is a process of fermentation.</p>
                        <p>Take your chosen tomato and slice it in half across the middle (it's "equator"). With a spoon or your well-washed fingers scoop out the seeds and their gelatinous "goo" into a clean cup or container. Add a couple of tablespoons of water to the seeds. Cover the container with a piece of plastic-wrap and then poke the plastic-wrap with a paring knife or pen point to put a small hole in it...this is to allow for air-transpiration. (A little fresh air needs to get in and out of the cup to help foster fermentation.)</p>
                    </div>
                </div>
                <div class="col-sm-4">
                    <div class="col-wrapper">
                        <div class="icon-wrapper"> <i class="fa  fa-cutlery"></i> </div>
                        <h3>Cooking Traditions</h3>
                        <p>Place the container of seeds in a warm location; a sunny windowsill or the top of the refrigerator are both excellent sites to place the container of seeds. Now Mother Nature will take over and begin to ferment the seed and water mixture. This takes about two or three days. Each night remove the plastic-wrap, stir the seed and water mixture, and then replace the plastic-wrap, if you use a new sheet of plastic-wrap then don't forget to put a small hole in it for air-transpiration. The top of the liquid will look "scummy" when the fermentation process has separated the "goo" from the seeds. It also helps destroy many of the possible tomato diseases that can be harbored by seeds.</p>
          				<p>Take the container of fermented seeds to the sink and with a spoon carefully remove the scummy surface. Then pour the container's contents into a fine kitchen sieve and rinse the seeds with water several times...stir them while they're in the sieve to assure that all surfaces are thoroughly rinsed. Give a few sharp taps to the sieve to help remove as much loose water as possible from the seeds.
		  				</p>
                    </div>
                </div>
                <div class="col-sm-4">
                    <div class="col-wrapper">
                        <div class="icon-wrapper"> <i class="fa fa-coffee"></i> </div>
                        <h3>Food Quality</h3>
                        <p>Line an open plate with a piece of waxed paper or a large automatic-drip coffee filter. Place the rinsed seeds onto the wax paper or coffee filter and spread them about so they are in a single layer. Place the plate in a safe location where the seeds can dry for a few days. Stir the seeds a few times during the drying process to assure that all their surfaces are evenly dry. Spread them out again into a single layer after each time you've stirred them. Tomato seeds are thick and can take up to a week to dry thoroughly. If you're having a rainy week that drying time may lengthen by a few days. Dried seeds move quickly and easily across a plate, they do not stick to each other.</p>
                        <p>To store them use paper packets or some folks like plastic. Whichever envelope style you choose is a matter of personal preference. If you choose to store your seeds in plastic the seeds must be BONE DRY....otherwise any moisture in the seeds will be transferred to all seeds inside the plastic packet, it will foster mildew and rotting and the seeds will be ruined. </p>
