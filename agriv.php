            <h2 class="section-title text-center">How to label them?</h2>
            <p class="lead main text-center"></p>

                        <div class="row">
                <div class="col-sm-3 col-md-3">
                    <div class="menu-images "><img src="https://41.media.tumblr.com/bfd11a1b6b6ffab454a4bc067f34627b/tumblr_ntebgxU6941ud7rr3o1_400.jpg" alt="LambBancon"></div>
                    <div class="menu-titles"><h1 class="">Labelling Tomatoes</h1></div>
                    <div class="menu-items ">
                    	<p>Tomatoes are generally self-pollinated so there is rarely a chance of cross-breeding. If you save and trade your seeds you might wish to describe your trade offering as "open-pollinated" tomato seeds. 
						That way the trader knows that Mother Nature was solely involved in the fertilization of the flower which produced the tomato that you have saved seeds from.
						Onto the packet write the tomato variety name (if you know it) or a very good description if you don't, add the term "open-pollinated" if you're sharing or trading your tomato seeds, and also add the current year to the packet description. Do enjoy saving tomato seeds and growing your own tomatoes at home from them.</p>
                    </div>
                </div>
                <div class="col-sm-3 col-md-3">
                    <div class="menu-images "><img src="https://40.media.tumblr.com/c468fcb7d4f4362b0de01e7e374bcfd5/tumblr_nted40Ix4z1ud7rr3o1_400.jpg" alt="El Dorado"></div>
                    <div class="menu-titles"><h1 class="">How to make a good nursery bed </h1></div>
                    <div class="menu-items ">
                        <p>A nursery bed is a prepared plot of land for raising seedlings. It acts a temporary home for young plants until they are eventually planted in a main garden.</p>
						<p>The nursery should be near the main garden to avoid damage of seedlings during transplanting. The water source should also be near for irrigation. The nursery bed should be protected from strong winds and should never be located on a slope to avoid erosion of seeds and nutrients; it should face east-west direction and should never be put in a water logged area.</p>
						<p>A poorly constructed and managed nursery bed will lead to poor germination or diseased seedlings. The seedlings may fail to grow when transplanted.</p>
						<h4> Bed Preparation</h4>
						<p>After site selection, clear the land. Measure 1m wide of any convenient length, dig it well at least to a depth of 0.3-0.45m so that stones and roots lying underneath are dug out and thrown outside the nursery. If the soil is gravely, it should be sieved through a wire mesh to remove gravel.
						The soil should be mixed with farmyard manure with one wheelbarrow of compost and sand for 3m2 to make at least a 7cm layer. Charcoal dust and ashes can also be mixed in the bed to correct the acidity of soil and keep away worms. Get dry grass lay on top of the nursery bed and burn, and later mix with the soil.</p>
						<h4> Planting Seeds</h4>
						<p>The bed should be watered thoroughly on the day prior to sowing. Seeds should be planted at a spacing of 30cm between rows, cover the seeds lightly with sand, charcoal dust and mulch the bed with dry grass. Mulching a nursery bed soon after sowing seeds is a must.</p>
						<h4>Maintenance of Nursery Bed</h4>
						<p>Mulch the seed bed after sowing until germination. In about four (4) to seven (7) days the seeds will have germinated. Provide enough shade to the top after germination 1m above the bed. Thin the crops properly, remove weak or diseased seedlings and keep the bed weed-free.</p>
						<h4>Transplanting</h4>
						<p>Transplant seedlings after 21 days; harden the crop by removing the shade a day before transplanting as this gives the seedling chance to get used to the direct sun.</p>
						<p>Reduce water at this stage. Transplant at recommended spacing per crop early in the morning or late in the evening (from 6:00am to 10:00am or 4:00pm to 6:00pm) and plants should receive water as soon as transplanting is done.</p>
                    </div>
                </div>
                <div class="col-sm-3 col-md-3">
                    <div class="menu-images "><img src="https://41.media.tumblr.com/5892daadaed762fbc9b3553a1b876a04/tumblr_nteecactZc1ud7rr3o1_1280.jpg" alt="Smoothies"></div>
                    <div class="menu-titles"><h1 class="">How to prepare the soil for tomato plants</h1></div>
                    <div class="menu-items ">
                        <p>Available in a variety of shapes and sizes, tomato plants are compact, with distinct heights depending on the breed. While there are several types of tomato plants available for the home garden, all varieties have a short harvest period, as well as specific planting needs. Soil is a main factor in planting nearly all types of tomatoes.</p>
						<h4>Here are some tips for preparing soil to produce healthy tomatoes.</h4>
						<p>Choose a soil for planting your tomatoes that is well drained, deep and loamy (rich with sand, silt and clay).</p>
						<p>Test the soils acidity. Tomatoes favor a more acidic soil with a pH of 6.2 -6.8, use a pH soil testing kit available at home improvement and garden stores to test the pH levels of the soil.</p>
						<p>Choose an area where the soil receives at least 6 hours of direct sunlight.</p>
						<p>Work the soil to prepare it for planting. Using a trowel or shovel, move the soil while it’s dry. Working with wet soil will make it difficult to move and aerate, and it will stick to your tools. If soil pH conditions aren’t ideal for growing tomato plants, add fertilizer to prepare tour soil for planting.</p>
						<p>Supplement the soil. Apply peat moss, compost or manure to the soil to improve its quality. Mix in small amounts of any or each of these components as you dig and mix your soil prior to planting. The richer the soil the better the growing conditions.</p>
						<p>Chooses an area where the soil is deep. Tomato plants require being set deeply in the soil, up to their first leaves.</p>
						<p>Purchase fertilizer with a 5-10-5 ratio of N-P-K (Nitrogen, Phosphorus, and Potassium).</p>
						<p>Prepare the fertilizer, dissolve 2 tablespoon (30ml) of fertilizer into 1 gallon (3.8l) of water. Apply 1 cup (240ml) of the solution to the base of each tomato plant, for larger areas; apply 2lbs (907g) of fertilizer per 100 square feet.</p>
						<p>Use grass clippings. Spread them around in your garden. The more the merrier to about 2-3 inches (5.1-7.6cm) height. Not only does this keep the weeds down but it also keeps the soil cool and moist, so less watering. It also provides more organic material (compost fertilizer) for the next growing season.</p>
						<p>Water about once a week in the morning only. Do not water at night, as this creates dangerous conditions for your plants as insects love wet dark environments and rot and other diseases such as mold, verticillium rot etc, are easily avoided by watering during the morning.</p>
						<p>Save those egg shells. When planting put crashed egg shells into the soil (if from seed, mix in from plants, in the hole you dig). The reward is calcium helps the plant avoid blossom rot.</>
                    </div>
                </div>
                <div class="col-sm-3 col-md-3">
                    <div class="menu-images "><img src="https://41.media.tumblr.com/6f3ee3d900b7018d98e9a63a6877ced9/tumblr_ntee05e9Y41ud7rr3o1_1280.jpg" alt="TastyBoxTasty"></div>
                    <div class="menu-titles"><h1 class="">Tomato Diseases</h1></div>
                    <div class="menu-items ">
                        <p>Late blight is a fungal disease that can strike during any part of the growing season. It will cause grey, moldy spots on leaves and fruit which later turn brown. The disease is spread and supported by persistent damp weather. This disease will overwinter, so all infected plants should be destroyed.</p>
						<p>Cracking: When fruit growth is too rapid, the skin will crack. This usually occurs in uneven water or uneven moisture due to weather conditions (very rainy periods mixed with dry periods). Keep moisture levels constant with consistent watering and mulching.</p>
						<p>Tobacco mosaic virus creates distorted leaves and causes young growth to be narrow and twisted, and leaves become molted with yellow. Unfortunately infected plans should be destroyed (but don’t put them in your compost pile).</p>
                    </div>
